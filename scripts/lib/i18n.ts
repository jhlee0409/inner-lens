export const I18N: Record<OutputLanguage, I18nStrings> = {
  en: I18N_EN,
  ko: I18N_KO,
  ja: I18N_JA,
  zh: I18N_ZH,
  es: I18N_ES,
  de: I18N_DE,
  fr: I18N_FR,
  pt: I18N_PT,
};

/**
 * Get i18n strings for the specified language
 */
export function getI18n(language: OutputLanguage): I18nStrings {
  return I18N[language] || I18N.en;
}

/**
 * Validate and get output language from environment
 */
export function getOutputLanguage(): OutputLanguage {
  const lang = process.env['OUTPUT_LANGUAGE']?.toLowerCase() || 'en';
  const validLanguages: OutputLanguage[] = ['en', 'ko', 'ja', 'zh', 'es', 'de', 'fr', 'pt'];
  return validLanguages.includes(lang as OutputLanguage) ? (lang as OutputLanguage) : 'en';
}
